#[test]
#[cfg_attr(miri, ignore)] // Calls RSYieldableMetric_Concat FFI in push_borrowed
fn reuse_results_optimization_quick_mode() {
    let (children, data) = create_mock_2([3], [2]);
    let mut union = UnionQuickHeap::new(children);

    let result = union
        .read()
        .expect("read failed")
        .expect("should have result");
    assert_eq!(result.doc_id, 2);
    assert_eq!(
        data[0].read_count(),
        1,
        "child0 should be read once for initial heap setup"
    );
    assert_eq!(
        data[1].read_count(),
        1,
        "child1 should be read once for initial heap setup"
    );

    let result = union
        .read()
        .expect("read failed")
        .expect("should have result");
    assert_eq!(result.doc_id, 3);
    // In heap quick mode, read() calls skip_to(last_doc_id + 1) = skip_to(3).
    // child1 (at doc 2) is below target 3, so it gets advanced (skip_to → EOF).
    // child0 (at doc 3) is already at target, so it's reused (no extra read).
    assert_eq!(
        data[0].read_count(),
        1,
        "child0 at doc 3 matches target - reused (no read needed)"
    );
    // child1 was at doc 2 < target 3, so the heap advances it via skip_to
    // which internally calls read(), incrementing read_count.
    assert_eq!(
        data[1].read_count(),
        2,
        "child1 was at doc 2 < target 3, advanced via skip_to"
    );

    let result = union.read().expect("read failed");
    assert!(result.is_none());
    assert_eq!(
        data[0].read_count(),
        2,
        "child0 should now be read to discover EOF"
    );
    assert_eq!(
        data[1].read_count(),
        2,
        "child1 already at EOF, no additional read"
    );
}

