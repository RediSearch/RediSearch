use std::{ffi::c_char, ptr::NonNull};

use result_processor::sorting_vector::{RSSortingVector, RSValueTrait};

pub const RS_SORTABLES_MAX: usize = 1024;

/// [RSValueFFI] is a wrapper around the C struct `RSValue` implement as new-type over a `*mut ffi::RSValue`.
///
/// It implements the `[Clone]` and `[Drop]` traits to manage the reference counting of the underlying C struct.
///
/// Safety:
/// 1. The pointer must be a valid pointer to an `RSValue` created by the C side.
#[repr(transparent)]
pub struct RSValueFFI(*mut ffi::RSValue);

impl Clone for RSValueFFI {
    fn clone(&self) -> Self {
        // Safety: We assume a valid ptr is given by the C side, and we are incrementing the reference count.
        unsafe { ffi::RSValue_IncrRef(self.0) };
        RSValueFFI(self.0)
    }
}

impl Drop for RSValueFFI {
    fn drop(&mut self) {
        // Safety: We assume a valid ptr is given by the C side, and we are decrementing the reference count.
        unsafe { ffi::RSValue_Decref(self.0) };
    }
}

impl RSValueTrait for RSValueFFI {
    fn create_null() -> Self {
        // Safety: RS_NullVal returns an immutable global ptr
        let val = unsafe { ffi::RS_NullVal() };
        RSValueFFI(val)
    }

    fn create_string(s: &str) -> Self {
        let len = s.len();

        // Safety: len is a valid size for allocation, and we allocate enough space for the string plus a null terminator.
        let ptr: *mut u8 = unsafe { libc::calloc(1, len + 1) }.cast();

        if ptr.is_null() {
            panic!("Failed to allocate memory");
        }

        // Safety: `ptr` is valid and large enough.
        unsafe {
            std::ptr::copy_nonoverlapping(s.as_ptr(), ptr, len);
        }

        // Safety: we know that `ptr` has length `len + 1`, so we can safely add `len` to it.
        let ptr_last = unsafe {
            ptr.add(len) // Ensure null termination
        };

        // Safety: We can safely write a null terminator at the end of the string.
        unsafe {
            *ptr_last = 0; // Null-terminate the string
        }

        // Safety: RSValue_FromString expects a valid pointer to a C string and its length.
        RSValueFFI(unsafe { ffi::RS_StringVal(ptr.cast(), len as u32) })
    }

    fn create_num(num: f64) -> Self {
        // Safety: RSValue_FromDouble expects a valid double value.
        let num = unsafe { ffi::RS_NumVal(num) };
        RSValueFFI(num)
    }

    fn create_ref(value: Self) -> Self {
        RSValueFFI(value.0)
    }

    fn is_null(&self) -> bool {
        // Safety: RS_NullVal returns an immutable global ptr
        self.0 == unsafe { ffi::RS_NullVal() }
    }

    fn get_ref(&self) -> Option<&Self> {
        // Safety: We assume a valid ptr is given by the C side
        let p = unsafe { self.0.as_ref() }.unwrap();
        if p.t() == ffi::RSValueType_RSValue_Reference {
            // Safety: We tested that the type is a reference, so we access it over the union safely.
            let ref_ptr = unsafe { p.__bindgen_anon_1.ref_ };

            // Safety: We assume that a valid pointer is given by the C side
            Some(unsafe { &*(ref_ptr as *const RSValueFFI) })
        } else {
            None
        }
    }

    fn get_ref_mut(&mut self) -> Option<&mut Self> {
        // Safety: We assume a valid ptr is given by the C side
        let p = unsafe { self.0.as_mut() }.unwrap();
        if p.t() == ffi::RSValueType_RSValue_Reference {
            // Safety: We tested that the type is a reference, so we access it over the union safely.
            let ref_ptr = unsafe { p.__bindgen_anon_1.ref_ };

            // Safety: We assume that a valid pointer is given by the C side
            Some(unsafe { &mut *(ref_ptr as *mut RSValueFFI) })
        } else {
            None
        }
    }

    fn as_str(&self) -> Option<&str> {
        // Safety: We assume a valid ptr is given by the C side
        let p = unsafe { self.0.as_ref() }.unwrap();
        if p.t() == ffi::RSValueType_RSValue_String {
            // Safety: We tested that the type is a string, so we access it over the union safely.
            let c_str: *mut c_char = unsafe { p.__bindgen_anon_1.strval }.str_;

            // Safety: We assume that a valid C string is generated by the C side (valid and null-terminated).
            Some(unsafe { std::ffi::CStr::from_ptr(c_str).to_str().unwrap() })
        } else {
            None
        }
    }

    fn as_num(&self) -> Option<f64> {
        // Safety: We assume a valid ptr is given by the C side
        let p = unsafe { self.0.as_ref() }.unwrap();
        if p.t() == ffi::RSValueType_RSValue_Number {
            // Safety: We tested that the type is a number, so we access it over the union safely.
            Some(unsafe { p.__bindgen_anon_1.numval })
        } else {
            None
        }
    }

    fn get_type(&self) -> ffi::RSValueType {
        // Safety: We assume a valid ptr is given by the C side
        let p = unsafe { self.0.as_ref() }.unwrap();
        p.t()
    }
}

/// Gets a RSValue from the sorting vector at the given index.
///
/// Safety:
/// 1. The pointer must be a valid pointer to an [`RSSortingVector`] created by [`NewSortingVector`].
#[unsafe(no_mangle)]
unsafe extern "C" fn RSSortingVector_Get(
    mut vec: NonNull<RSSortingVector<RSValueFFI>>,
    idx: libc::size_t,
) -> *mut ffi::RSValue {
    // Safety: Caller must ensure 1. --> Deref is safe
    let vec = unsafe { vec.as_mut() };
    if idx >= vec.len() {
        return std::ptr::null_mut();
    }

    vec[idx].0
}

/// Returns the length of the sorting vector.
///
/// Safety:
/// 1. The pointer must be a valid pointer to an [`RSSortingVector`] created by [`NewSortingVector`] or null.
#[unsafe(no_mangle)]
unsafe extern "C" fn RSSortingVector_GetLength(
    vec: *const RSSortingVector<RSValueFFI>,
) -> libc::size_t {
    if vec.is_null() {
        return 0;
    }

    // Safety: Caller must ensure 1. --> Deref is safe, we checked for null above
    unsafe { vec.as_ref() }.unwrap().len() as libc::size_t
}

/// Returns the memory size of the sorting vector.
///  
/// Safety:
/// 1. The pointer must be a valid pointer to an [`RSSortingVector`] created by [`NewSortingVector`].
#[unsafe(no_mangle)]
unsafe extern "C" fn RSSortingVector_GetMemorySize(
    vector: NonNull<RSSortingVector<RSValueFFI>>,
) -> libc::size_t {
    // Safety: Caller must ensure 1. --> Deref is safe
    unsafe { vector.as_ref() }.get_memory_size() as libc::size_t
}

